// c language to demonstrate AND operator
#include<stdio.h>
int main()
{
    int num = 5;
    //printing the result with AND operator
    printf("%d\n",(num==10 && num>=5));
    printf("%d\n",(num>=5 && num<=50));
    printf("%d\n",(num!=10 && num>=5));
    printf("%d\n",(num>=20 && num<=50));
    return 0;
    
}